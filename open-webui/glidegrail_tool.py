    def __init__(self):
        self.valves = self.Valves()
        self._lock = threading.Lock()
        self._sections: "OrderedDict[str, Tuple[str, str]]" = OrderedDict()
        self._source_sig = ""
        self._file_state: Tuple[str, float] = ("", -1.0)
        # (path, mtime) of a file version known NOT to provide the document
        # (zero sections, or refused as older than the cached parse) —
        # memoized so it is skipped without re-reading on every message.
        self._bad_file_state: Tuple[str, float] = ("", -1.0)
        self._url_fetched_at = 0.0
        self._last_failure_at = 0.0
        # Current retry delay; doubles per consecutive failure (capped).
        self._backoff_s = float(FETCH_RETRY_BACKOFF_S)

    # -- diagnostics --------------------------------------------------------

    def _log(self, message: str) -> None:
        if bool(self.valves.debug):
            print(f"[glidegrail-tool] {message}")

    # -- document loading (mirrors the filter; blocking, call via to_thread) --

    def _fetch_url(self, url: str) -> str:
        import requests  # lazy import by design

        response = requests.get(
            url,
            timeout=10,
            headers={"User-Agent": "glidegrail-openwebui-tool/1.1.0"},
        )
        response.raise_for_status()
        return response.text

    def _ensure_document(self) -> "OrderedDict[str, Tuple[str, str]]":
        """Return parsed sections — NEVER empty while the embedded copy
        decodes, and never raises. Resolution: source_path (when present
        AND actually containing the document) > source_url (TTL-cached;
        failures and garbage responses back off with a doubling delay) >
        last good live parse (stale beats the snapshot) > embedded copy.
        A live source yielding ZERO sections is treated as unavailable,
        never cached as fresh content. Blocking I/O happens here (callers
        use asyncio.to_thread); the lock is never held across I/O or the
        embedded decode (module-level memo, own lock)."""
        try:
            live = self._load_live()
        except Exception as exc:  # absolute safety net — never raises
            self._log(f"live document load failed unexpectedly: {exc}")
            live = None
        if live:
            return live
        with self._lock:
            if self._sections:
                # A previously loaded live copy, even stale, beats the
                # shipped snapshot.
                return self._sections
        return _embedded_document()[1]

    def _load_live(self) -> Optional["OrderedDict[str, Tuple[str, str]]"]:
        """Try the LIVE sources (file, then URL). Returns None when neither
        provides a non-empty parse right now — _ensure_document applies the
        embedded floor at that single chokepoint."""
        path = (self.valves.source_path or "").strip()
        if path and os.path.isfile(path):
            sections = self._load_file(path)
            if sections:
                return sections
            # Unreadable or document-less file: ignored (loudly, in the
            # log) rather than allowed to disable the fallbacks below.
        url = (self.valves.source_url or "").strip()
        if url:
            return self._load_url(url)
        return None

    def _load_file(
        self, path: str
    ) -> Optional["OrderedDict[str, Tuple[str, str]]"]:
        try:
            mtime = os.path.getmtime(path)
            sig = f"file:{path}:{mtime}"
            with self._lock:
                if sig == self._source_sig:
                    return self._sections  # serving exactly this version
                if self._bad_file_state == (path, mtime):
                    # Memoized: this file version provides no document.
                    return None
            with open(path, "r", encoding="utf-8", errors="replace") as fh:
                text = fh.read()
            parsed = _parse_sections(text)
            with self._lock:
                if not parsed:
                    self._bad_file_state = (path, mtime)
                    self._log(
                        f"source_path file has no sections, ignoring: {path}"
                    )
                    return None
                cached_path, cached_mtime = self._file_state
                # Never let a racing OLDER read clobber a newer parse.
                if self._source_sig != sig and not (
                    cached_path == path and mtime < cached_mtime
                ):
                    self._sections = parsed
                    self._source_sig = sig
                    self._file_state = (path, mtime)
                    self._bad_file_state = ("", -1.0)
                    self._log(
                        f"loaded {len(parsed)} sections from file {path}"
                    )
                elif self._source_sig != sig:
                    # Refused as older than the cached parse: memoize so it
                    # is not re-read every message; a touch/edit self-heals.
                    self._bad_file_state = (path, mtime)
                return self._sections if self._sections else None
        except OSError as exc:
            self._log(f"file read failed ({path}): {exc}")
            return None

    def _register_fetch_failure(self) -> None:
        """Stamp the negative cache and DOUBLE the retry delay (capped), so
        an offline install stops paying fetch latency on a fixed cadence."""
        with self._lock:
            self._last_failure_at = time.time()
            self._backoff_s = min(
                self._backoff_s * 2, FETCH_RETRY_BACKOFF_MAX_S
            )

    def _load_url(
        self, url: str
    ) -> Optional["OrderedDict[str, Tuple[str, str]]"]:
        ttl_seconds = max(int(self.valves.cache_ttl_minutes), 1) * 60
        url_sig = f"url:{url}"
        now = time.time()
        with self._lock:
            if (
                self._source_sig == url_sig
                and (now - self._url_fetched_at) < ttl_seconds
            ):
                return self._sections  # fresh cache of a GOOD fetch
            if (
                self._last_failure_at
                and (now - self._last_failure_at) < self._backoff_s
            ):
                return None  # negative cache; caller serves stale/embedded

        try:
            text = self._fetch_url(url)
        except Exception as exc:
            self._log(f"url fetch failed ({url}): {exc}")
            self._register_fetch_failure()
            return None
        parsed = _parse_sections(text)
        if not parsed:
            # HTTP 200 but not the document (captive portal, proxy block
            # page): a failure in disguise — never cache it as fresh.
            self._log(f"url returned no sections, treating as failure: {url}")
            self._register_fetch_failure()
            return None

        with self._lock:
            fetched_at = time.time()
            if not (
                self._source_sig == url_sig
                and (fetched_at - self._url_fetched_at) < ttl_seconds
            ):
                self._sections = parsed
                self._source_sig = url_sig
                self._url_fetched_at = fetched_at
                # Let a reappearing file take over again (any mtime).
                self._file_state = ("", -1.0)
                self._log(f"loaded {len(parsed)} sections from url {url}")
            self._last_failure_at = 0.0
            self._backoff_s = float(FETCH_RETRY_BACKOFF_S)
            return self._sections if self._sections else None

